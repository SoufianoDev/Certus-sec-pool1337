#include <unistd.h>

int	main(void)
{
	char	a;
	char	b;

	a = 'a';
	while (a <= 'z')
	{
		b = a + 1;
		b = b - 32;
		write(1, &a, 1);
		write(1, &b, 1);
		a += 2;
	}
	write(1, "\n", 1);
	return (0);
}
