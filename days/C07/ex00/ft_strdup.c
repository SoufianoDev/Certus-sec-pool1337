char	*ft_strdup(char *src)
{
	int		i;
	char	*dest;

	dest = malloc(i * sizeof(char));
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
