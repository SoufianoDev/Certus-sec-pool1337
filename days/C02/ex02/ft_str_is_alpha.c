int	ft_str_is_alpha(char *str)
{
	int	i;
	int	a;
	int	b;

	i = 0;
	while (str[i] != '\0')
	{
		a = (str[i] <= 'Z' && str[i] >= 'A');
		b = (str[i] <= 'z' && str[i] >= 'a');
		if (!a && !b)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
